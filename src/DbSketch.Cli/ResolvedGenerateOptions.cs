using DimonSmart.DbSketch.Core.Config;
using DimonSmart.DbSketch.Core.Rendering;
using DimonSmart.DbSketch.Core.Schema;

namespace DimonSmart.DbSketch.Cli;

public sealed record ResolvedGenerateOptions(string Provider, string ConnectionString, string OutputPath, DiagramFormat DiagramRenderer, OutputFormat Output, bool Verbose, bool DryRun, SchemaFilterOptions Filter, DiagramRenderOptions Diagram, bool ReadComments);

public static class GenerateOptionsResolver
{
    public static ResolvedGenerateOptions Resolve(DbSketchConfig config, CliOptions cli)
    {
        var provider = NormalizeProvider(cli.Provider ?? config.Provider);
        var connectionString = cli.ConnectionString ?? config.ConnectionString;
        if (string.IsNullOrWhiteSpace(connectionString))
        {
            throw new CliException("Missing connection string.");
        }

        var diagramRenderer = DiagramRendererParser.Parse(cli.Renderer ?? config.Diagram.Renderer);
        var outputFormat = OutputContainerFormatParser.Parse(cli.Format ?? config.Output.Format);
        var direction = DiagramDirectionParser.Parse(config.Diagram.Direction);
        var diagramTitle = string.IsNullOrWhiteSpace(config.Diagram.Title) ? "Database schema" : config.Diagram.Title;
        var markdownOptions = outputFormat == OutputContainerFormat.Markdown
            ? new MarkdownRenderOptions(
                GetMarkdownFenceLanguage(config.Output.Markdown.FenceLanguage, diagramRenderer),
                config.Output.Markdown.Header ?? $"# {diagramTitle}{Environment.NewLine}{Environment.NewLine}Generated by DbSketch.",
                config.Output.Markdown.Footer)
            : null;

        return new ResolvedGenerateOptions(
            provider,
            connectionString,
            cli.OutputPath ?? config.Output.Path ?? "dbsketch.dot",
            diagramRenderer,
            new OutputFormat(outputFormat, markdownOptions),
            cli.Verbose,
            cli.DryRun,
            new SchemaFilterOptions(config.Include.Tables, config.Exclude.Tables),
            new DiagramRenderOptions(
                diagramTitle,
                direction,
                config.Diagram.Compact,
                new DiagramShowOptions(config.Diagram.Show.SchemaName, config.Diagram.Show.ColumnTypes, config.Diagram.Show.Nullability, config.Diagram.Show.PrimaryKeys, config.Diagram.Show.ForeignKeys, config.Diagram.Show.Comments),
                new MermaidRenderOptions(config.Diagram.Mermaid.EmitDirection)),
            config.Comments.Enabled);
    }

    private static string GetMarkdownFenceLanguage(string? configuredLanguage, DiagramFormat renderer)
    {
        if (!string.IsNullOrWhiteSpace(configuredLanguage))
        {
            return configuredLanguage;
        }

        return renderer switch
        {
            DiagramFormat.Dot => "dot",
            DiagramFormat.Mermaid => "mermaid",
            _ => throw new ArgumentOutOfRangeException(nameof(renderer), renderer, null)
        };
    }

    public static string NormalizeProvider(string? provider)
    {
        if (string.IsNullOrWhiteSpace(provider))
        {
            throw new CliException("Missing provider.");
        }

        return provider.Trim().ToLowerInvariant() switch
        {
            "mssql" => "sqlserver",
            "sqlserver" => "sqlserver",
            "postgresql" => "postgres",
            "postgres" => "postgres",
            "mysql" => "mysql",
            var value => throw new CliException($"Unknown provider '{value}'. Supported values: sqlserver, postgres, mysql.")
        };
    }
}
